
var students = 20;          //  number type

var firstName = 'Gosho';    // string type

var trueOrFalse = true;     // boolean type

var sym = Symbol();         //  symbol type

var nul = null;            //  null type

var lastName;               // undefined type

var arr = [1,2,3,5,7,7,9];  // object type



console.log(typeof(students));      // result is number

console.log(typeof(firstName));     // result is string

console.log(typeof(trueOrFalse));   // result is boolean

console.log(typeof(sym));           // result is symbol

console.log(typeof(nul));           // result is object !

console.log(typeof(lastName));      // result is undefined

